public class printSyntax {
    public static void main(String[] args){
        System.out.println("Xin chao Java!");
        System.out.println("Hello" + " " + "Ducknife");
        System.out.printf("%.2f", 1.23123);  /* định dạng in số thập phân hoặc các kiểu dữ liệu khác. */
        System.out.print("Hello");
        System.out.print("hihi");
    }
}

/* printf() và print() không có dấu in xuống dòng ở cuối, còn println() thì có. */